package mighty13.minterin;

public class ListVideo {

    public String judulVideo;
    public int duration;

    public ListVideo(String judulVideo, int duration) {
        this.judulVideo = judulVideo;
        this.duration = duration;
    }

    public String getJudulVideo() {
        return judulVideo;
    }

    public int getDuration() {
        return duration;
    }
}
